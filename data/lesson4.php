(function initLesson4Task7SoftCheck() {

  const normalize = (s) => {
    if (!s) return '';
    return s
      .toLowerCase()
      .replaceAll('ё', 'е')
      // убираем пунктуацию, тире, кавычки
      .replace(/[.,;:!?()[\]{}"“”«»'’—–-]/g, ' ')
      // сжимаем пробелы
      .replace(/\s+/g, ' ')
      .trim();
  };

  document.querySelectorAll('#l4-task7 .l-soft-input[data-answers]').forEach(input => {
    input.addEventListener('input', () => {
      const fb = input.parentElement.querySelector('.feedback');
      if (!fb) return;

      const userRaw = input.value;
      const user = normalize(userRaw);

      if (!user) {
        fb.textContent = '';
        fb.className = 'feedback small mt-1';
        return;
      }

      const variantsRaw = input.dataset.answers.split('|').map(v => v.trim()).filter(Boolean);
      const variants = variantsRaw.map(normalize);

      const ok = variants.includes(user);

      fb.className = 'feedback small mt-1 fw-semibold ' + (ok ? 'text-success' : 'text-danger');
      fb.textContent = ok ? 'Правильно' : 'Проверьте (можно без запятых/в другом порядке слов)';
    });
  });

})();
