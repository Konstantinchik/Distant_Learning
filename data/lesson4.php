document.addEventListener("DOMContentLoaded", () => {
    const options = [
        "Системная плата", "Шина", "Слот",
        "Видеокарта", "Звуковая карта",
        "Сетевая карта", "Блок питания"
    ];

    document.querySelectorAll(".answer").forEach(sel => {
        let opts = [...options].sort(() => Math.random() - 0.5);
        opts.forEach(text => {
            let o = document.createElement("option");
            o.textContent = text;
            sel.appendChild(o);
        });
    });

    document.getElementById("checkTask2").onclick = () => {
        let correct = 0;
        document.querySelectorAll(".answer").forEach(sel => {
            sel.style.borderColor = "#ced4da";
            if (!sel.value) return;
            if (sel.value === sel.dataset.answer) {
                correct++;
                sel.style.borderColor = "green";
            } else {
                sel.style.borderColor = "red";
            }
        });
        document.getElementById("resultTask2").textContent =
            `Правильно: ${correct} из 7`;
    };
});
</script>
